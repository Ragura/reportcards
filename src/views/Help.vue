      bekijken kan het zijn dat het inladen van de inhoud eventjes duurt. Ook
      dit is normaal.
    </p>
    <p>
      De marges van de PDF staan standaard correct ingesteld, maar kunnen in
      geval van noodzaak via de instellingen veranderd worden.
    </p>
