    2. Verwijder het huidige rapport langs verkenner (bv. 1920_1A_1.rap)<br />
    3. Verander de naam van een backup bestand door de suffix te verwijderen.
    Met andere woorden, enkel de naam van het originele rapport mag overblijven
    (bv. 1920_1A_1.rap).

