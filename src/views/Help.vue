        >deze link</a
      >.
    </p>
    <p>
      Dubbelklik op het gedownloade bestand om de installatie te starten. Het
      zou kunnen dat je zowel bij het downloaden als het installeren
      <strong>toestemming moet geven</strong> om het bestand te vertrouwen.
    </p>
    <p>Voorbeelden:</p>

    - bij het downloaden via Chrome kiezen voor &quot;behouden&quot;na het
