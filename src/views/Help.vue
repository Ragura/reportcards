    - bij het installeren kiezen voor &quot;meer informatie&quot; en dan

    <h2 id="de-applicatie-starten">De applicatie starten</h2>
    <p>
      Bij het opstarten van de applicatie kom je terecht op het hoofdscherm
      &quot;Home&quot;. Hier kan je rechtstreeks een nieuw rapport maken of een
      bestaand rapport openen. Indien er een nieuwe versie van de applicatie
      beschikbaar is zal je daarvan een melding krijgen op dit scherm, waarna op
      de achtergrond de update gedownload wordt. Wanneer de update klaar is om
      te installeren krijg je ook hiervan een melding.
    </p>
    <h2 id="nieuw-rapport-maken">Nieuw rapport maken</h2>
    <p>
      Bij het aanmaken van een nieuw rapport hoor je de volgende zaken in te
      geven:
    </p>
    - Schooljaar (automatisch het huidige, maar kan worden veranderd)<br />
    - Leerjaar<br />
    - Klas<br />
    - Periode (herfst, winter, lente, zomer)
    <p>
      De combinatie van de verschillende keuzes bepaalt de
      <strong>vorm en inhoud</strong> van het rapport. Kijk dus na het maken van
      het rapport eerst even na of je wel degelijk het juiste rapport te zien
      krijgt.
    </p>
    <p>
      Elk rapport wordt opgeslagen als <strong>apart .rap bestand</strong> en
      kan worden <strong>doorgegeven aan anderen</strong> (of jezelf op een
      andere PC) die de applicatie ook geïnstalleerd hebben. Standaard worden de
      rapporten opgeslagen in de <strong>map documenten</strong> met een
      specifieke naam (bv. 1920_1A_1.rap).
    </p>
    <p>
      Een rapport bestaat uit drie onderdelen: <strong>Rapport</strong>,
      <strong>Leerlingen</strong> en <strong>Punten</strong>.
    </p>
    <h2 id="rapport">Rapport</h2>
    <p>
      Het onderdeel <strong>Rapport</strong> verschijnt automatisch na het
      aanmaken van een nieuw rapport of het openen van een bestaand rapport. Het
      is de <strong>visuele weergave</strong> van het rapport zoals het zal
      worden afgedrukt / omgezet naar PDF. Je kan er dus op rekenen dat de
      inhoud overeenkomt met de uiteindelijke afdruk. Selecteer de actieve
      leerling uit de uitklapbare lijst.
    </p>
    <p>
      Op het rapport kan je twee dingen rechtstreeks wijzigen: kleuren en
      commentaren. Elk geëvalueerd onderdeel dat een uiteindelijke kleur krijgt
      toegewezen kan veranderd worden door met de muis
      <strong>overheen het kleurvak</strong> te bewegen. Commentaren kunnen
      worden getypt in de daarvoor voorziene blokken.
    </p>
    <p><strong>Elke wijziging wordt automatisch opgeslagen!</strong></p>
    <h2 id="leerlingen">Leerlingen</h2>
    <p>
      Het onderdeel <strong>Leerlingen</strong> geeft een overzicht van de
      leerlingen die tot de klas behoren. Een nieuw rapport bevat geen
      leerlingen, maar deze kunnen worden toegevoegd door:
    </p>

    - Manueel in te voeren (voornaam &amp; familienaam)<br />
    - Importeren vanuit bestaand rapport (bv. vorige periode)<br />
    - Importeren vanuit een Wisa Excel bestand (langs secretariaat).

    <p>
      Als je een leerling verwijdert, verwijder je ook de bijhorende punten en
      commentaren.
    </p>
    <h2 id="punten">Punten</h2>
    <p>
      Het onderdeel Punten biedt je de mogelijkheid per onderdeel en per
      leerling punten, kleuren of codes in te voeren. Hiervoor worden standaard
      6 evaluaties voorzien per leerling. Je hoeft ze echter niet allemaal in te
      vullen! Gebruik alleen de evaluaties die je echt nodig hebt.
    </p>
    <p>
      Elke invoer draagt bij tot het berekenen van een
      <strong>gemiddelde</strong> per leerling. Dit gemiddelde verschijnt
      automatisch op het rapport. Je krijgt ook voor jezelf een klasgemiddelde
      en totaal gemiddelde te zien per onderdeel, maar deze worden nergens op
      het rapport getoond.
    </p>
    <p>
      Elke evaluatie staat op een aantal punten. Standaard is dit op 10 punten,
      maar dit kan worden aangepast via de rij met
      <strong>maxima</strong> onderaan een puntentabel. De gemiddeldes worden
      automatisch aangepast. Een hoger maximum weegt zwaarder door op het
      leerlingengemiddelde.
    </p>
    <p>
      Bij onderdeel waarop via kleuren of codes wordt geëvalueerd
      <strong>telt alleen de laatste kolom</strong> (gemiddelde). De kleuren bij
      individuele evaluaties zijn alleen voor jou bedoeld als hulpmiddel om een
      overzicht bij te houden en een goed eindoordeel te kunnen vellen.
    </p>
    <p><strong>Elke wijziging wordt automatisch opgeslagen!</strong></p>
    <h2 id="pdf-maken">PDF Maken</h2>
    <p>
      Bij de weergave van het rapport kan je via de knop
      <strong>Printen</strong> een PDF bestand genereren. Let op: het maken van
      een PDF kan even duren. Wacht het proces geduldig af vooraleer het
      programma af te sluiten.
    </p>
    <p>
      Verschiet niet van de grootte van het PDF-bestand. Het is normaal dat ze
